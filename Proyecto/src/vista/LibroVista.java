    private void jButtonAgregarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAgregarLibroActionPerformed
        int anio;
        String titulo, autor, categoria, valor;
        boolean disponible;
        
        valor = this.jTextFieldTitulo.getText();
        if(!v.validaVacio(valor)){
            JOptionPane.showMessageDialog(this, "Titulo es obligatorio.", "Validacion.", JOptionPane.WARNING_MESSAGE);
            this.jTextFieldTitulo.requestFocus();
            return ;
        }
        titulo = valor;
        
        valor = this.jTextFieldAutor.getText();
        if(!v.validaVacio(valor)){
            JOptionPane.showMessageDialog(this, "Autor es obligatorio.", "Validacion.", JOptionPane.WARNING_MESSAGE);
            this.jTextFieldAutor.requestFocus();
            return ;
        }
        autor = valor;
        
        valor = this.jTextFieldAnio.getText();
        if(!v.validaVacio(valor)){
            JOptionPane.showMessageDialog(this, "Año del libro es obligatorio.", "Validacion.", JOptionPane.WARNING_MESSAGE);
            this.jTextFieldAnio.requestFocus();
            return ;
        }
        if(!v.validaInt(valor)){
            JOptionPane.showMessageDialog(this, "Año debe ser un entero.", "Validacion.", JOptionPane.WARNING_MESSAGE);
            this.jTextFieldAutor.requestFocus();
            return ;
        }
        anio = Integer.parseInt(valor);
        
        categoria = (String) jComboBoxCategoria.getSelectedItem();
        
        disponible = true;
        
        Libro libro = new Libro(titulo, autor, anio, categoria, disponible);
        
        if(c.add(libro)){
            JOptionPane.showMessageDialog(this, "Libro agregado correctamente.", "Informacion.", JOptionPane.INFORMATION_MESSAGE);
            this.jTextFieldAutor.requestFocus();
        }else{
            JOptionPane.showMessageDialog(this, "Libro NO agregado.", "Error.", JOptionPane.ERROR_MESSAGE);
            this.jTextFieldAutor.requestFocus();
        }
        cargar();
        
    }//GEN-LAST:event_jButtonAgregarLibroActionPerformed

