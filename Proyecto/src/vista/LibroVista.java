    public void cargar(){
        int idLibro, anio;
        String titulo, autor, categoria;
        boolean disponible;
        
        DefaultTableModel modelo = (DefaultTableModel)this.jTableLibro.getModel();
        modelo.setRowCount(0);
        ArrayList<Libro> lista = c.listlibro();
        
        for (Libro tmp : lista) {
            idLibro = tmp.getIdLibro();
            titulo = tmp.getTitulo();
            autor = tmp.getAutor();
            anio = tmp.getAnio();
            categoria = tmp.getCategoria();
            disponible = tmp.isDisponible();
            modelo.addRow(new Object[]{idLibro,titulo,autor,anio,categoria,disponible});
        }
        lista = null;
    }
