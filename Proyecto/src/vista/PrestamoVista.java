    private void jButtonDevolucionLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDevolucionLibroActionPerformed
        int idPrestamo, idUsuario = 0, idLibro = 0;
        Date fechaPrestamo = null, fechaEstimada = null, fechaDevolucion = null;
        String valor;
        
        
        valor = this.txtIdPrestamoDevolucion.getText();
        if(!v.validaVacio(valor)){
            JOptionPane.showMessageDialog(this, "Id del usuario es obligatorio.", "Validacion.", JOptionPane.WARNING_MESSAGE);
            this.txtIdPrestamoDevolucion.requestFocus();
            return ;
        }
        if(!v.validaInt(valor)){
            JOptionPane.showMessageDialog(this, "Id debe ser un entero.", "Validacion.", JOptionPane.WARNING_MESSAGE);
            this.txtIdPrestamoDevolucion.requestFocus();
            return ;
        }
        idPrestamo = Integer.parseInt(valor);
        
        for (Prestamo p : c.listprestamo()) {
            if(idPrestamo == p.getIdPrestamo()){
                idPrestamo = p.getIdPrestamo();
                idUsuario = p.getIdUsuario();
                idLibro = p.getIdLibro();
                fechaPrestamo = p.getFechaPrestamo();
                fechaEstimada = p.getFechaEstimada();
                break;
            }
        }
        Date hoy = new Date();
        Calendar cal = Calendar.getInstance();
        cal.setTime(hoy);
        Prestamo prestamo = new Prestamo(idPrestamo, idUsuario, idLibro, fechaPrestamo, fechaEstimada, hoy);
        
        if(c.act(prestamo)){
            JOptionPane.showMessageDialog(this, "Usuario actualizado correctamente.", "Informacion.", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Usuario NO actualizado.", "Error.", JOptionPane.ERROR_MESSAGE);
        }
        Libro libro = new Libro(true);
        c.sidisponible(libro);
        cargar();
        
    }//GEN-LAST:event_jButtonDevolucionLibroActionPerformed

