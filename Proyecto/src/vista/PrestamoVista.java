    private void jButtonRegistrarPrestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegistrarPrestActionPerformed
        int idu, idl;
        String valor;
        
        valor = this.txtUsuarioPrest.getText();
        if(!v.validaVacio(valor)){
            JOptionPane.showMessageDialog(this, "Rut es obligatorio.", "Validacion.", JOptionPane.WARNING_MESSAGE);
            this.txtUsuarioPrest.requestFocus();
            return ;
        }
        if(!v.validaInt(valor)){
            JOptionPane.showMessageDialog(this, "Id del usuario debe ser entero", "Validacion", JOptionPane.WARNING_MESSAGE);
            this.txtLibroPrest.requestFocus();
        }
        idu = Integer.parseInt(valor);
        for (Usuario u : c.listusuario()) {
            if(idu == u.getIdUsuario()){
                idu = u.getIdUsuario();
                break;
            }
        }
        valor = this.txtLibroPrest.getText();
        if(!v.validaVacio(valor)){
            JOptionPane.showMessageDialog(this, "Id del libro es obligatorio.", "Validacion.", JOptionPane.WARNING_MESSAGE);
            this.txtLibroPrest.requestFocus();
            return ;
        }
        if(!v.validaInt(valor)){
            JOptionPane.showMessageDialog(this, "Id del libro debe ser entero", "Validacion", JOptionPane.WARNING_MESSAGE);
            this.txtLibroPrest.requestFocus();
        }
        idl = Integer.parseInt(valor);
        for (Libro l : c.listlibro()) {
            if(idl == l.getIdLibro()){
                idl = l.getIdLibro();
                break;
            }
        }
        Date hoy = new Date();
        Calendar cal = Calendar.getInstance();
        cal.setTime(hoy);
        cal.add(Calendar.MONTH, 2);
        Date fechaEstimada = cal.getTime();
        
        Prestamo pres = new Prestamo(idu, idl, hoy, fechaEstimada,null);
        
        if(c.add(pres)){
            JOptionPane.showMessageDialog(this, "Prestamo registrado correctamente.", "Informacion.", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Prestamo NO registrado.", "Error.", JOptionPane.ERROR_MESSAGE);
        }
        Libro libro = new Libro(false);
        c.nodisponible(libro);
        cargar();
    }//GEN-LAST:event_jButtonRegistrarPrestActionPerformed

